    private void refrescarTabla() {
        // Crea un modelo de tabla con las columnas que necesitas
        DefaultTableModel model = new DefaultTableModel(
                new Object[][]{}, 
                new String[]{"Identificaci√≥n", "Nombres", "Apellidos", "Edad", "Carrera ID"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // ninguna celda editable
            }
        };

        // Obtiene la lista de personas
        List<EstudianteDto> listaEstudiantes = controlador.obtenerEstudiantes();

        // Llena el modelo fila por fila
        for (EstudianteDto estudiantes : listaEstudiantes) {
            model.addRow(new Object[]{
                estudiantes.getIdentificacion(),
                estudiantes.getNombres(),
                estudiantes.getApellidos(),
                estudiantes.getEdad(),
                estudiantes.getCarreraId()
        });
            
        }

        tableSistemaClientes.setModel(model);
    }

