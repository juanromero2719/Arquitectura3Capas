    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
         
        try {

            int  idCarrera = Integer.parseInt(tfId.getText());
            String nombre = tfNombre.getText();
            int  facultadId = Integer.parseInt(tfFacultad.getText());
            
            controlador.agregarCarrera(nombre, facultadId);

            refrescarTabla();

        } catch (NumberFormatException ex) {
            System.out.println("❌ Error: Formato de número inválido. " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        
        try {
            int idCarrera = Integer.parseInt(tfId.getText());
            controlador.eliminarCarrera(idCarrera);
            refrescarTabla();

        } catch (NumberFormatException ex) {
            System.err.println("❌ Error en formato numérico: " + ex.getMessage());
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        
        try {
            int idCarrera = Integer.parseInt(tfId.getText());
            String nombre = tfNombre.getText();
            int facultadId = Integer.parseInt(tfFacultad.getText());

            controlador.actualizarCarrera(idCarrera, nombre, facultadId);

            refrescarTabla();

        } catch (NumberFormatException ex) {
            System.err.println("❌ Error en formato numérico: " + ex.getMessage());
        }
    }//GEN-LAST:event_BtnEditarActionPerformed


